package calculos;

public class Funcoes {
	public double soma(double a, double b) {
		double resultado = a + b;
		return resultado;
	}
	
	public double subt(double a, double b) {
		double resultado = a - b;
		return resultado;
	}
	
	public double divi(double a, double b) {
		double resultado = a / b;
		return resultado;
	}
	
	public double mult(double a, double b) {
		double resultado = a * b;
		return resultado;
	}
	

}
